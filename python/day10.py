def p1(data):
    cycle = 1
    sums = 0
    X = 1

    for line in data:
        if line == "noop":
            cycles = 1
            add_value = 0
        else:
            cycles = 2
            _, count_str = line.split()
            add_value = int(count_str)

        for _ in range(cycles):
            if not (cycle + 20) % 40 or cycle == 20:
                sums += X * cycle
            cycle += 1
        else:
            X += add_value

    return sums


def p2(data):
    cycle = 1
    X = 1
    render = [["."] * 40 for _ in range(6)]

    for line in data:
        if line == "noop":
            cycles = 1
            add_value = 0
        else:
            cycles = 2
            _, count_str = line.split()
            add_value = int(count_str)

        for _ in range(cycles):
            render_row = (cycle - 1) // 40
            render_col = (cycle - 1) % 40

            if X in [
                render_col - 1,
                render_col,
                render_col + 1,
            ]:
                render[render_row][render_col] = "#"
            cycle += 1

        else:
            X += add_value

    render_str = "\n".join(["".join(x) for x in render])
    return render_str


def run(data):
    print(p1(data))
    print(p2(data))


